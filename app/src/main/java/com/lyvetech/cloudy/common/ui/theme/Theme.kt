@Composable
fun CloudyTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    disableDynamicTheming: Boolean = false,
    content: @Composable () -> Unit,
) {
    val colorScheme = when {
        !disableDynamicTheming && isSupportingDynamicTheming() -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        else -> if (darkTheme) DarkThemeColors else LightThemeColors
    }

    // Composition locals
    CompositionLocalProvider {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = WaffleeTypography,
            content = content,
        )
    }

    val systemUiController = rememberSystemUiController()

    systemUiController.setStatusBarColor(
        color = colorScheme.background,
        darkIcons = !darkTheme
    )
    systemUiController.setNavigationBarColor(
        color = colorScheme.background,
        darkIcons = !darkTheme
    )
}

@ChecksSdkIntAtLeast(api = Build.VERSION_CODES.S)
fun isSupportingDynamicTheming() = Build.VERSION.SDK_INT >= Build.VERSION_CODES.S